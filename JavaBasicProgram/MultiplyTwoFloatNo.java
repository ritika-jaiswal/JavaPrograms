// Java Program to Multiply Two Floating-Point Numbers

package JavaBasicProgram;

public class MultiplyTwoFloatNo {
    public static void main(String[] args) {
        float num1, num2, num3;
        num1 = 3.2f;
        num2 = 22.3f;
        num3 = num1 * num2;
        System.out.println(num3);
    }

}
